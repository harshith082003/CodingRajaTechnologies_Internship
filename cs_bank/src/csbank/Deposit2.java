    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            
            String accNo = txtacc.getText();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cs_bank", "root", "");
            
            insert = conn.prepareStatement("select c.cust_id, c.firstname, c.lastname, a.balance from customers c, accounts a where c.cust_id = a.cust_id and a.acc_id = ?");
            insert.setString(1, accNo);
            
            ResultSet rs = insert.executeQuery();
            
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this,"Account not Found");
            } else {
                String id = rs.getString(1);
                String firstname = rs.getString(2);
                String lastname = rs.getString(3);
                String balance = rs.getString(4);
                
                
                jLabel10.setText(id.trim());
                jLabel11.setText(firstname.trim());
                jLabel13.setText(lastname.trim());
                jLabel9.setText(balance.trim());
                
            }
            
            
              
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Deposit2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed


