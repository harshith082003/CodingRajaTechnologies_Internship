    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            conn.setAutoCommit(false);
            
            String accNo = txtacc.getText();
            String cust_id = jLabel10.getText();
            
            String date = jLabel12.getText();
           
            String balance = jLabel9.getText();
            String amount = txtamount.getText();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cs_bank", "root", "");
            
            insert = conn.prepareStatement("insert into deposits(acc_id, cust_id, date, balance, deposit) values(?,?,?,?,?)");            
            insert.setString(1, accNo);
            insert.setString(2, cust_id);
            insert.setString(3, date);
            insert.setString(4, balance);
            insert.setString(5, amount);
            
            insert.executeUpdate();
            
            update = conn.prepareStatement("update accounts set balance = balance + ? where acc_id = ?");
            update.setString(1, amount);
            update.setString(2, accNo);
            update.executeUpdate();
            
            
            JOptionPane.showMessageDialog(this,"Amount Deposited");
            conn.commit();
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            try {
                conn.rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(Deposit2.class.getName()).log(Level.SEVERE, null, ex1);
            }
            Logger.getLogger(Deposit2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

